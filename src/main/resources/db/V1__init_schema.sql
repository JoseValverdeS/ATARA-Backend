-- 21. REGISTRO DE AUDITORÍA -- DATOS FLEXIBLES
-- ============================================================
-- Tabla de auditoría que registra cada acción realizada en el sistema por los usuarios.
CREATE TABLE registro_auditoria (
    id                 INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id         INT
                           REFERENCES usuarios(id) ON DELETE SET NULL
                           DEFERRABLE INITIALLY IMMEDIATE,
    accion             VARCHAR(50)  NOT NULL,   -- INSERT, UPDATE, DELETE, LOGIN, LOGOUT, VIEW, EXPORT
    tabla_nombre       VARCHAR(100),
    registro_id        BIGINT,                  -- BIGINT para soportar futuros cambios de PK
    valores_anteriores JSONB,
    valores_nuevos     JSONB,
    ip_origen          INET,
    user_agent         VARCHAR(500),
    created_at         TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);

-- Índice para consultar el historial de acciones de un usuario específico.
CREATE INDEX idx_audit_usuario ON registro_auditoria (usuario_id);
-- Índice para consultar el historial de cambios sobre una tabla específica.
CREATE INDEX idx_audit_tabla   ON registro_auditoria (tabla_nombre);
-- Índice para filtrar el registro de auditoría por tipo de acción.
CREATE INDEX idx_audit_accion  ON registro_auditoria (accion);
-- Índice para consultar el registro de auditoría en orden cronológico descendente.
CREATE INDEX idx_audit_fecha   ON registro_auditoria (created_at DESC);

COMMENT ON TABLE  registro_auditoria             IS 'Log inmutable de acciones de usuario para seguridad y cumplimiento.';
COMMENT ON COLUMN registro_auditoria.registro_id IS 'BIGINT para soportar PKs de cualquier tipo sin migración futura.';


-- ============================================================
-- FUNCIÓN Y TRIGGERS — AUTO-UPDATE de updated_at
-- ============================================================

-- Función que actualiza automáticamente el campo updated_at con la fecha y hora actual antes de cada modificación.
CREATE OR REPLACE FUNCTION fn_actualizar_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

COMMENT ON FUNCTION fn_actualizar_updated_at IS 'Asigna updated_at = NOW() antes de cada UPDATE.';

